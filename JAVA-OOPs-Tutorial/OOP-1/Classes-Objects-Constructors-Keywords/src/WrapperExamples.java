public class WrapperExamples {
    public static void main(String[] args) {
        // Primitive Type
        int a = 45;
        System.out.println(a);

        // Wrapper Classes ----> Converting Primitives to Objects
        Integer num = 45;
        System.out.println(num); // Here num is an Object

        // final Keyword
        final int AGE = 24;
//        AGE = 34; ----> You cant change it.

    }
}
